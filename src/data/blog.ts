export function getBlogPosts(locale: "tr" | "en") {
  return blogPosts.filter((post) => post.locale === locale);
}

export function getBlogPostBySlug(slug: string, locale: "tr" | "en") {
  return blogPosts.find((post) => post.slug === slug && post.locale === locale);
}

export function getRelatedPosts(slug: string, locale: "tr" | "en") {
  return getBlogPosts(locale).filter((post) => post.slug !== slug).slice(0, 2);
}
